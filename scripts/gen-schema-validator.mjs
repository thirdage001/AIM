// Build-time codegen: compile the §15 JSON Schema (and the draft profile) into
// a STANDALONE ESM validator module using Ajv. The committed output contains no
// `new Function`/`eval`, so it runs on Cloudflare Workers (which forbid dynamic
// code generation) as well as Node and Deno.
//
// Run via `pnpm gen:schema`. CI re-runs this and `git diff --exit-code`s the
// output, which also satisfies spec §3.5 ("aim.core generated or checked
// against the schema").

import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import Ajv2020 from "ajv/dist/2020.js";
import addFormats from "ajv-formats";
import standaloneCode from "ajv/dist/standalone/index.js";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, "..");

const manifestSchema = JSON.parse(
  readFileSync(join(root, "schema/manifest.schema.json"), "utf8")
);
const draftSchema = JSON.parse(
  readFileSync(join(root, "schema/draft.schema.json"), "utf8")
);

const ajv = new Ajv2020({
  code: { source: true, esm: true },
  allErrors: true,
  strict: false
});
addFormats(ajv);

ajv.addSchema(manifestSchema, "Manifest");
ajv.addSchema(draftSchema, "Draft");

const moduleCode = standaloneCode(ajv, {
  validateManifest: "Manifest",
  validateDraft: "Draft"
});

const outDir = join(root, "packages/core/src/validate/generated");
mkdirSync(outDir, { recursive: true });
const header =
  "// @ts-nocheck\n" +
  "/* GENERATED by scripts/gen-schema-validator.mjs — do not edit by hand. */\n" +
  "/* eslint-disable */\n";
writeFileSync(join(outDir, "validate-manifest.mjs"), header + moduleCode, "utf8");

console.log("Wrote packages/core/src/validate/generated/validate-manifest.mjs");
